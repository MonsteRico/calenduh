import type { DateTime } from "luxon";
// import { signIn, signOut, useSession } from "next-auth/react";
import Head from "next/head";
import React, { useContext, useState } from "react";
import MonthView from "~/components/monthView";
import { ThemeToggle } from "~/components/themeToggle";
import { Tabs, TabsContent, TabsList, TabsTrigger } from "~/components/ui/tabs";
import WeekView from "~/components/weekView";
import { CurrentViewContext, DayBeingViewedContext } from "~/hooks/contexts";
import { useToday } from "~/hooks/useToday";

// import { api } from "~/utils/api";

export default function Home() {
  //const hello = api.post.hello.useQuery({ text: "from tRPC" });
  const today = useToday();
  const [dayBeingViewed, setDayBeingViewed] = useState<DateTime<true>>(today);
  const [currentView, setCurrentView] = useState<"month" | "week" | "day">(
    "month",
  );

  return (
    <CurrentViewContext.Provider
      value={{ value: currentView, setValue: setCurrentView }}
    >
      <DayBeingViewedContext.Provider
        value={{ value: dayBeingViewed, setValue: setDayBeingViewed }}
      >
        <Head>
          <title>Calenduh</title>
          <meta name="description" content="Generated by create-t3-app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>
        <Tabs defaultValue="month">
          <main>
            <TopBar />
            <TabsContent value="month">
              <MonthView />
            </TabsContent>
            <TabsContent value="week">
              <WeekView />
            </TabsContent>
            <TabsContent value="day">
              <h1>Day</h1>
            </TabsContent>
          </main>
        </Tabs>
      </DayBeingViewedContext.Provider>
    </CurrentViewContext.Provider>
  );
}

function TopBar() {
  const today = useToday();
  const { value: dayBeingViewed, setValue: setDayBeingViewed } = useContext(
    DayBeingViewedContext,
  );
  const { value: currentView, setValue: setCurrentView } =
    useContext(CurrentViewContext);
  return (
    <div className="sticky top-0 z-10 flex flex-row justify-center gap-8 bg-background py-4">
      <h2
        onClick={() => {
          setDayBeingViewed(dayBeingViewed.minus({ month: 1 }));
        }}
        className="cursor-pointer"
      >
        {"<"}
      </h2>
      <h2>
        {dayBeingViewed.monthLong} {dayBeingViewed.year}
      </h2>
      <h2
        onClick={() => {
          setDayBeingViewed(dayBeingViewed.plus({ month: 1 }));
        }}
        className="cursor-pointer"
      >
        {">"}
      </h2>
      <h2
        onClick={() => {
          setDayBeingViewed(today);
        }}
        className="cursor-pointer underline"
      >
        Today
      </h2>
      <TabsList>
        <TabsTrigger value="month">Month</TabsTrigger>
        <TabsTrigger value="week">Week</TabsTrigger>
        <TabsTrigger value="day">Day</TabsTrigger>
      </TabsList>
      <ThemeToggle />
    </div>
  );
}
